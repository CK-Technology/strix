use leptos::prelude::*;

/// Icon size variants.
#[derive(Clone, Copy, Default)]
pub enum IconSize {
    /// 16x16 pixels
    Small,
    /// 20x20 pixels (default)
    #[default]
    Medium,
    /// 24x24 pixels
    Large,
    /// 32x32 pixels
    XLarge,
}

impl IconSize {
    fn class(&self) -> &'static str {
        match self {
            IconSize::Small => "w-4 h-4",
            IconSize::Medium => "w-5 h-5",
            IconSize::Large => "w-6 h-6",
            IconSize::XLarge => "w-8 h-8",
        }
    }
}

