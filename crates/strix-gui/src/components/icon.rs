/// Helper function to get icon by string name (for backwards compatibility with sidebar).
pub fn get_icon_by_name(name: &str) -> AnyView {
    let icon_name = match name {
        "dashboard" => IconName::Dashboard,
        "folder" => IconName::Folder,
        "users" => IconName::Users,
        "group" => IconName::Group,
        "policy" => IconName::Policy,
        "key" => IconName::Key,
        "access-key" => IconName::AccessKey,
        "chart" => IconName::Chart,
        "log" => IconName::Log,
        "audit" => IconName::Audit,
        "bell" => IconName::Bell,
        "config" => IconName::Config,
        "settings" => IconName::Settings,
        "license" => IconName::License,
        "openid" => IconName::OpenId,
        _ => return view! { <span class="w-5 h-5"></span> }.into_any(),
    };

    view! { <Icon name=icon_name /> }.into_any()
}
